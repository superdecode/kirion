export const useI18nStore = create(
  persist(
    (set, get) => ({
      locale: 'es',
      setLocale: (locale) => set({ locale }),
      t: (key) => {
        const { locale } = get()
        return translations[locale]?.[key] || translations.es[key] || key
      },
    }),
    { name: 'wms-i18n', partialize: (s) => ({ locale: s.locale }) }
  )
)
